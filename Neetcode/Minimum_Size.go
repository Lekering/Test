package main

// Функция находит минимальную длину подмассива, сумма которого не меньше target
func minSubArrayLen(target int, nums []int) int {
	// Шаг 1: Инициализируем переменные.[
	// l — левый указатель окна (начальный индекс подмассива)
	// total — текущая сумма элементов окна
	// res — результат (минимальная длина), изначально устанавливаем как длина + 1
	l, total := 0, 0
	res := len(nums) + 1

	// Шаг 2: Итерируемся по правому указателю окна
	for r := range nums {
		// Добавляем текущий элемент к сумме
		total += nums[r]
		// Шаг 3: Сдвигаем левый указатель, пока сумма больше или равна target
		for total >= target {
			// Обновляем результат, если текущая длина меньше ранее найденной
			if (r - l + 1) < res {
				res = r - l + 1
			}
			// Убираем слева элемент из окна и двигаем левый указатель вправо
			total -= nums[l]
			l++
		}
	}

	// Шаг 4: Если не найдено подходящего подмассива, возвращаем 0
	if res == len(nums)+1 {
		return 0
	}
	// Шаг 5: Иначе возвращаем минимальную длину
	return res
}
